# 內建範本清單（name → content 對應）
BUILTIN_PROMPTS = {
    "Dcard 好物推薦文": DEFAULT_SYSTEM_PROMPT,
    "Google 排名衝刺版": DEFAULT_SYSTEM_PROMPT_V2,
}


def seed_default_prompts(db: Session):
    """啟動時同步所有內建範本（新增 / 更新 / 保持不動）"""
    for name, content in BUILTIN_PROMPTS.items():
        existing = (
            db.query(PromptTemplate)
            .filter(PromptTemplate.is_builtin == True, PromptTemplate.name == name)
            .first()
        )
        if existing:
            if existing.content != content:
                existing.content = content
                db.commit()
                logger.info(f"已同步更新內建範本: {name}")
            else:
                logger.info(f"內建範本已是最新: {name}")
        else:
            # 第一個範本設為預設（若 DB 中尚無預設）
            has_default = db.query(PromptTemplate).filter(PromptTemplate.is_default == True).first()
            template = PromptTemplate(
                name=name,
                content=content,
                is_default=(not has_default),
                is_builtin=True,
            )
            db.add(template)
            db.commit()
            logger.info(f"已建立內建範本: {name}")


def get_default_prompt(db: Session, user_id: int | None = None) -> str:
    """取得預設範本內容（per-user：先找用戶自訂預設，再找內建預設）"""
    if user_id:
        user_default = db.query(PromptTemplate).filter(
            PromptTemplate.user_id == user_id,
            PromptTemplate.is_default == True,
        ).first()
        if user_default:
            return user_default.content

    # fallback: 內建預設
    builtin_default = db.query(PromptTemplate).filter(
        PromptTemplate.is_builtin == True,
        PromptTemplate.is_default == True,
    ).first()
    if builtin_default:
        return builtin_default.content

    return DEFAULT_SYSTEM_PROMPT
